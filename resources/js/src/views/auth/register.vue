<template>
    <div class="form full-form auth-cover">
        <div class="form-container">
            <div class="form-form">
                <div class="form-form-wrap">
                    <div class="form-container">
                        <div class="form-content">
                            <h1 class="">
                                Crear una cuent de voluntario en <br />
                                Mano Amiga
                            </h1>
                            <p class="signup-link">¿Ya tienes una cuenta? <router-link to="/auth/login">Inicia sesión</router-link></p>
                            <Form @submit="handleRegister" :validation-schema="schema">
                                <div class="form">
                                    <div class="field-wrapper input">
                                        <svg
                                            xmlns="http://www.w3.org/2000/svg"
                                            width="24"
                                            height="24"
                                            viewBox="0 0 24 24"
                                            fill="none"
                                            stroke="currentColor"
                                            stroke-width="2"
                                            stroke-linecap="round"
                                            stroke-linejoin="round"
                                            class="feather feather-user"
                                        >
                                            <path d="M20 21v-2a4 4 0 0 0-4-4H8a4 4 0 0 0-4 4v2"></path>
                                            <circle cx="12" cy="7" r="4"></circle>
                                        </svg>
                                        <Field name="fullname" type="text" class="form-control" placeholder="Nombre completo"/>
                                        <ErrorMessage name="fullname" class="error-feedback text-danger" />
                                    </div>
                                    <div class="field-wrapper input">
                                        <svg
                                            xmlns="http://www.w3.org/2000/svg"
                                            width="24"
